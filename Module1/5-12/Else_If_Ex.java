package com.a512;

public class Else_If_Ex 
{
	public static void main(String[] args)
	{
		
		int marks = 47;
		
		if(marks>=70)
		{
			System.out.println("A Grade");
		}
		else if(marks>=60)
		{
			System.out.println("B Grade");
		}
		else if(marks>=50)
		{
			System.out.println("C Grade");
		}
		else if(marks>=40)
		{
			System.out.println("D Grade");
		}
		else 
		{
			System.out.println("Fail");
		}
		
	}
}
